package com.gigawattstechnology.writeout;

public class User {
    String name;
    String url ;

    public User ()
    {

    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getName() {
        return name;
    }
}
